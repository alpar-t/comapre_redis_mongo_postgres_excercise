def is_restricted_number(number, is_trial=True):
    """
    Lookup all prefixes of a number in our restricted country code list.


    > [number[0:idx] for idx in range(1, len(number))]
    ['1', '17', '176', '1763', '17632', '176322', '1763222', '17632225', '176322255', '1763222555']


    Regex for plivo phone file:
    /^\(\d\+\)\s\+\(.*\)$/    \"\1\"\: \"\2\"\,/g
    """
    if is_trial:
        number_list = RESTRICTED_OUTBOUND_TRIAL_PREFIXES
    else:
        number_list = RESTRICTED_OUTBOUND_PAYING_PREFIXES

    if number.startswith('+'):
        number = number[1:]

    for prefix in [number[0:idx] for idx in range(1, len(number) + 1)]:
        if number_list.get(prefix):
            return True
    return False
